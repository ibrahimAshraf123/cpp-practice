

#include <iostream>
using namespace std;

int main()
{
    short int A, B;

    cout << "please enter the 1st number A? ";
    cin >> A;
    cout << endl;

    cout << "please enter the 2nd number B? ";
    cin >> B;
    cout << endl;
    
    cout << A << " + " << B << " = " << A + B << endl;
    cout << A << " - " << B << " = " << A - B << endl;
    cout << A << " * " << B << " = " << A * B << endl;
    cout << A << " / " << B << " = " << A / B << endl;
    cout << A << " % " << B << " = " << A % B << endl;

    return 0;
}



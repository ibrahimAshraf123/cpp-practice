

#include <iostream>
using namespace std;

int main()
{
	int A, B;
	cout << "Please enter the 1st number A? \n";
	cin >> A;
	cout << "\n";

	cout << "Please enter the 2nd number B? \n";
	cin >> B;
	cout << "\n";

	cout << (A == B) << "\n";
	cout << (A != B) << "\n";
	cout << (A > B) << "\n";
	cout << (A < B) << "\n";
	cout << (A >= B) << "\n";
	cout << (A <= B) << "\n";

	return 0;
}


